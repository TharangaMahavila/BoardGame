class Member:
    def __init__(
            self,
            fname,
            lname,
            street,
            city,
            postal,
            phone,
            email,
            password
            ):
        self.first_name = fname
        self.last_name = lname
        self.street = street
        self.city = city
        self.postal = postal
        self.phone = phone
        self.email = email
        self.password = password
